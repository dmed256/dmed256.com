    <?php highlight('OCCA') ?> is an open-source (<?php highlight('MIT license') ?>) library used to program current multi-core/many-core architectures.
    Devices (such as <?php highlight2('CPUs, GPUs, Intel\'s Xeon Phi, FPGAs') ?>, etc) are abstracted using an offload-model for application development and programming for the devices is done through a C-based (<?php highlight('OKL') ?>) or Fortran-based kernel language (<?php highlight('OFL') ?>).
    OCCA gives developers the ability to target devices at run-time by using run-time compilation for device kernels.

